/* ****
  * 获取 ETOR BUS ID
  * 返回值：uint8 型 范围为0-15
  *  */
uint8_t get_id(void)
{
	uint8_t id = 0;
	if( HAL_GPIO_ReadPin(ID1_GPIO_Port,ID1_Pin) != GPIO_PIN_SET )
	{
		//ID第一位 1
		id |= 0x01;
	}
	if( HAL_GPIO_ReadPin(ID2_GPIO_Port,ID2_Pin) != GPIO_PIN_SET )
	{
		//ID第二位 2
		id |= 0x02;
	}
	if( HAL_GPIO_ReadPin(ID3_GPIO_Port,ID3_Pin) != GPIO_PIN_SET ) 
	{
		//ID第三位 3
		id |= 0x04;
	}
	if( HAL_GPIO_ReadPin(ID4_GPIO_Port,ID4_Pin) != GPIO_PIN_SET )
	{
		//ID第四位 4
		id |= 0x08;
	}		
	return id;
}



